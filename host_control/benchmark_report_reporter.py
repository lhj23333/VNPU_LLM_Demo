"""Append rows to results/benchmark_report.md (RK3588_LLM MarkdownReporter-compatible table)."""

from __future__ import annotations

from pathlib import Path
from typing import Any

from .common import REPO_ROOT, aggregate_dram_from_metrics


def _report_path() -> Path:
    return REPO_ROOT / "results" / "benchmark_report.md"


def _row_from_task_result(task_result: dict, summary: dict[str, Any]) -> str:
    meta = task_result.get("report_meta") or {}
    model_name = str(meta.get("model_name") or task_result.get("task_id", ""))
    max_context = int(meta.get("max_context_len", 0))
    npu_core = int(meta.get("rknn_core_num", 0))

    agg = aggregate_dram_from_metrics(task_result.get("metrics", []))
    init_mb = float(task_result.get("max_init_dram_weights_kv_mb", agg["max_init_dram_weights_kv_mb"]))
    runtime_mb = float(task_result.get("max_runtime_buffer_dram_mb", agg["max_runtime_buffer_dram_mb"]))
    peak_mb = float(task_result.get("max_total_peak_dram_mb", agg["max_total_peak_dram_mb"]))

    tps = float(summary.get("avg_generate_tps", 0.0))
    status = str(task_result.get("status", summary.get("status", "")))

    return (
        f"| {model_name} | {max_context} | "
        f"{npu_core} | ~{init_mb:.1f} MB | ~{runtime_mb:.1f} MB | ~{peak_mb:.1f} MB | "
        f"{tps:.2f} | {status} |\n"
    )


def append_benchmark_report_row(task_result: dict, summary: dict[str, Any]) -> Path:
    """Insert benchmark table rows before the first non-table line after the table (same strategy as RK3588)."""
    report_path = _report_path()
    report_path.parent.mkdir(parents=True, exist_ok=True)
    new_row = _row_from_task_result(task_result, summary)

    if report_path.exists():
        lines = report_path.read_text(encoding="utf-8").splitlines(keepends=True)
        insert_idx = -1
        for i, line in enumerate(lines):
            if line.startswith("|") and (i + 1 >= len(lines) or not lines[i + 1].startswith("|")):
                insert_idx = i + 1
        if insert_idx != -1:
            lines = lines[:insert_idx] + [new_row] + lines[insert_idx:]
            report_path.write_text("".join(lines), encoding="utf-8")
        else:
            with report_path.open("a", encoding="utf-8") as fp:
                fp.write(new_row)
    else:
        with report_path.open("w", encoding="utf-8") as fp:
            fp.write("# VNPU LLM/VLM Benchmark Report\n\n")
            fp.write("## 性能与内存深度剖析 (Performance & Memory Profiling)\n\n")
            fp.write(
                "| Model Name | Context Length | NPU Core | Init DRAM (Weights+KV-Cache) | "
                "Runtime Buffer DRAM | Total Peak DRAM (VmHWM) | Generate TPS | Status |\n"
            )
            fp.write("| :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n")
            fp.write(new_row)
            fp.write(
                "\n_Note: Automatically generated by host_control. "
                "Init DRAM sampled at demo ready (VmRSS); total peak prefers RKLLM Peak Memory Usage (GB) "
                "else VmHWM; runtime buffer = total peak − init._\n"
            )
    return report_path
